    public function index()
    {
        $teamHasard = DB::table('teams')
        ->inRandomOrder()
        ->limit(2)
        ->get();

        $sansEquipe = DB::table('players')
        ->where('team_id', '=', '8')
        ->inRandomOrder()
        ->limit(4)
        ->get();

        $avecEquipe = DB::table('players')
        ->whereNotIn('team_id', [8])
        ->inRandomOrder()
        ->limit(4)
        ->get();

        $equipeEurope = DB::table('teams')
        ->where('country_id', '=', 'Europe')
        ->inRandomOrder()
        ->get();

        $fille = DB::table('players')
        ->where('gender_id', '=', '2')
        ->whereNotIn('team_id', [8])
        ->inRandomOrder()
        ->get();

        $homme = DB::table('players')
        ->where('gender_id', '=', '1')
        ->whereNotIn('team_id', [8])
        ->inRandomOrder()
        ->get();



        return view('dashboard', compact('teamHasard', 'sansEquipe', 'avecEquipe', 'equipeEurope', 'fille', 'homme'));
    }

